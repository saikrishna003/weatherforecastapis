package com.springboot.weatherapis.rapidapiresponse;

public class Clouds {
	
	private int high;
    private int low;
    private int middle;
	public int getHigh() {
		return high;
	}
	public void setHigh(int high) {
		this.high = high;
	}
	public int getLow() {
		return low;
	}
	public void setLow(int low) {
		this.low = low;
	}
	public int getMiddle() {
		return middle;
	}
	public void setMiddle(int middle) {
		this.middle = middle;
	}
    
    
}
